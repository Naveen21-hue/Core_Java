package controlstatements;

public class WhileLoop {
	//while loop increment
	
	public static void main(String[] args) {
		int i=1;
		while(i<=10) { 
			System.out.print(i+ " ");
			i++;
		}
		
		//while decrement
				int j=10;
				while(j>=1) {
					System.out.print(j+" ");
					j--;
				}			
	}
}

			
